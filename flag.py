import turtle
t=turtle.Turtle()
t.speed(0)
t.hideturtle()
t.pensize(10)
t.up()
t.goto(0,-400)
t.down()
t.setheading(90)
t.fd(700)
t.begin_fill()
t.fillcolor("orange")
t.pensize(0)
t.right(90)
t.fd(300)
t.right(90)
t.fd(60)
t.left(90)
t.bk(300)
t.end_fill()
t.up()
t.begin_fill()
t.fillcolor("white")
t.fd(300)
t.down()
t.right(90)
t.fd(60)
t.left(90)
t.bk(300)
t.end_fill()
t.up()
t.begin_fill()
t.fillcolor("green")
t.fd(300)
t.right(90)
t.down()
t.fd(60)
t.left(90)
t.bk(300)
t.end_fill()
t.up()
t.goto(-40,240)
t.fd(200)
t.down()
t.color("blue")
t.circle(-30)
t.setheading(270)
t.fd(30)
t.setheading(0)
for i in range(24):
    t.fd(30)
    t.bk(30)
    t.left(15)
t.color("black")
t.up()
t.goto(-100,-300)
t.down()
t.begin_fill()
t.fillcolor("black")
t.fd(200)
t.right(90)
t.fd(100)
t.left(90)
t.bk(200)
t.left(90)
t.fd(100)
t.end_fill()



turtle.done()
